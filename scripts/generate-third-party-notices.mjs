function readNpmRuntimePackageEntries(packageLock) {
    const entryList = [];

    for (const [lockPath, packageEntry] of Object.entries(packageLock.packages ?? {})) {
        if (!lockPath.startsWith("node_modules/") || packageEntry.dev === true) {
            continue;
        }

        const packageName = lockPath.slice("node_modules/".length);

        entryList.push({
            name: packageName,
            version: packageEntry.version ?? "",
            license: normalizeLicense(packageEntry.license),
        });
    }

    return sortPackageEntries(entryList);
}

async function readNuGetRuntimePackageEntries(sourceWindowsDepsJsonPathList) {
    if (sourceWindowsDepsJsonPathList.length === 0) {
        throw new Error("--target source-windows requires at least one --source-windows-deps-json path from a published output.");
    }

    const packageByName = new Map();

    for (const depsJsonPath of sourceWindowsDepsJsonPathList) {
        const depsJson = await readJson(resolve(depsJsonPath));

        for (const packageEntry of readNuGetPackageEntriesFromDepsJson(depsJson)) {
            if (packageByName.has(packageEntry.name)) {
                continue;
            }

            const packageMetadata = await readNuGetPackageMetadata(packageEntry.name, packageEntry.version);
            packageByName.set(packageEntry.name, {
                name: packageEntry.name,
                version: packageEntry.version,
                license: packageMetadata.license,
                source: packageMetadata.source,
            });
        }
    }

    return sortPackageEntries([...packageByName.values()]);
}

function readNuGetPackageEntriesFromDepsJson(depsJson) {
    const packageEntryList = [];

    for (const [libraryName, libraryEntry] of Object.entries(depsJson.libraries ?? {})) {
        if (libraryEntry.type !== "package") {
            continue;
        }

        const separatorIndex = libraryName.lastIndexOf("/");
        if (separatorIndex <= 0 || separatorIndex === libraryName.length - 1) {
            continue;
        }

        packageEntryList.push({
            name: libraryName.slice(0, separatorIndex),
            version: libraryName.slice(separatorIndex + 1),
        });
    }

    return packageEntryList;
}

async function readNuGetPackageMetadata(packageName, packageVersion) {
    const packageDirectory = join(readNuGetPackageRoot(), packageName.toLowerCase(), packageVersion.toLowerCase());
    const nuspecPath = join(packageDirectory, `${packageName.toLowerCase()}.nuspec`);
    const nuspecText = await readOptionalText(nuspecPath);

    if (nuspecText === undefined) {
        throw new Error(`NuGet package metadata was not found: ${nuspecPath}. Restore source-windows projects before generating notices.`);
    }

    return {
        license: readNuGetLicense(nuspecText),
        source: readNuGetSource(nuspecText),
    };
}

function readNuGetPackageRoot() {
    if (process.env.NUGET_PACKAGES && process.env.NUGET_PACKAGES.trim().length > 0) {
        return process.env.NUGET_PACKAGES;
    }

    return join(homedir(), ".nuget", "packages");
}

function readNuGetLicense(nuspecText) {
    const licenseMatch = nuspecText.match(/<license\s+type="expression"\s*>(?<license>[^<]+)<\/license>/iu);
    if (licenseMatch?.groups?.license) {
        return decodeXmlText(licenseMatch.groups.license);
    }

    const licenseUrlMatch = nuspecText.match(/<licenseUrl>(?<licenseUrl>[^<]+)<\/licenseUrl>/iu);
    if (licenseUrlMatch?.groups?.licenseUrl) {
        return decodeXmlText(licenseUrlMatch.groups.licenseUrl);
    }

    return "See package";
}

function readNuGetSource(nuspecText) {
    const repositoryMatch = nuspecText.match(/<repository\b[^>]*\surl="(?<url>[^"]+)"/iu);
    if (repositoryMatch?.groups?.url) {
        return decodeXmlText(repositoryMatch.groups.url);
    }

    const projectUrlMatch = nuspecText.match(/<projectUrl>(?<projectUrl>[^<]+)<\/projectUrl>/iu);
    if (projectUrlMatch?.groups?.projectUrl) {
        return decodeXmlText(projectUrlMatch.groups.projectUrl);
    }

    return "";
}

function normalizeLicense(licenseValue) {
    if (typeof licenseValue === "string") {
        return licenseValue;
    }

    if (Array.isArray(licenseValue)) {
        return licenseValue.map(normalizeLicense).join(", ");
    }

    if (licenseValue && typeof licenseValue === "object" && typeof licenseValue.type === "string") {
        return licenseValue.type;
    }

    return "See package";
}

function formatNpmPackageTable(packageEntries) {
    return [
        "| Package | Version | License |",
        "| --- | --- | --- |",
        ...packageEntries.map(packageEntry => [
            escapeMarkdownTableCell(packageEntry.name),
            escapeMarkdownTableCell(packageEntry.version),
            escapeMarkdownTableCell(packageEntry.license),
        ].join(" | ")).map(row => `| ${row} |`),
    ].join("\n");
}

function formatPackageTable(packageEntries) {
    return [
        "| Package | Version | License | Source |",
        "| --- | --- | --- | --- |",
        ...packageEntries.map(packageEntry => [
            escapeMarkdownTableCell(packageEntry.name),
            escapeMarkdownTableCell(packageEntry.version),
            escapeMarkdownTableCell(packageEntry.license),
            formatSourceCell(packageEntry.source),
        ].join(" | ")).map(row => `| ${row} |`),
    ].join("\n");
}

function formatSourceCell(source) {
    if (source.length === 0) {
        return "";
    }

    const normalizedSource = source
        .replace(/^git\+/iu, "")
        .replace(/^git:\/\//iu, "https://")
        .replace(/^git@github\.com:/iu, "https://github.com/")
        .replace(/^ssh:\/\/git@github\.com\//iu, "https://github.com/")
        .replace(/\.git$/iu, "");

    if (/^https?:\/\//iu.test(normalizedSource)) {
        return `<${escapeMarkdownTableCell(normalizedSource)}>`;
    }

    if (/^[A-Za-z0-9_.-]+\/[A-Za-z0-9_.-]+$/u.test(normalizedSource)) {
        return `<https://github.com/${escapeMarkdownTableCell(normalizedSource)}>`;
    }

    return escapeMarkdownTableCell(normalizedSource);
}

function escapeMarkdownTableCell(value) {
    return String(value).replaceAll("|", "\\|").replaceAll("\n", " ");
}

function sortPackageEntries(packageEntries) {
    return packageEntries.sort((leftPackage, rightPackage) =>
        leftPackage.name.localeCompare(rightPackage.name, "en", { sensitivity: "base" }));
}

async function writeNoticeFileIfChanged(path, content) {
    const expectedContent = normalizeNoticeContent(content);
    const existingContent = await readOptionalText(path);
    if (existingContent === expectedContent) {
        return {
            path,
            changed: false,
        };
    }

    await writeFile(path, expectedContent, "utf8");
    return {
        path,
        changed: true,
    };
}

async function checkNoticeFile(path, content) {
    const expectedContent = normalizeNoticeContent(content);
    const existingContent = await readOptionalText(path);

    return {
        path,
        changed: existingContent !== expectedContent,
    };
}

function normalizeNoticeContent(content) {
    return `${content.replace(/\s+$/u, "")}\n`;
}

async function readJson(path) {
    return JSON.parse(await readFile(path, "utf8"));
}

async function readOptionalText(path) {
    try {
        return await readFile(path, "utf8");
    } catch {
        return undefined;
    }
}

function decodeXmlText(value) {
    return value
        .replaceAll("&amp;", "&")
        .replaceAll("&lt;", "<")
        .replaceAll("&gt;", ">")
        .replaceAll("&quot;", "\"")
        .replaceAll("&apos;", "'");
}

async function main(argumentList) {
    const targetList = [];
    const sourceWindowsDepsJsonPathList = [];
    let shouldCheck = false;

    for (let argumentIndex = 0; argumentIndex < argumentList.length; argumentIndex += 1) {
        const argument = argumentList[argumentIndex];
        if (argument === "--check") {
            shouldCheck = true;
            continue;
        }

        if (argument === "--target") {
            const target = argumentList[argumentIndex + 1];
            if (target === undefined || target.startsWith("--")) {
                throw new Error("--target requires a value.");
            }

            targetList.push(target);
            argumentIndex += 1;
            continue;
        }

        if (argument === "--source-windows-deps-json") {
            const depsJsonPath = argumentList[argumentIndex + 1];
            if (depsJsonPath === undefined || depsJsonPath.startsWith("--")) {
                throw new Error("--source-windows-deps-json requires a value.");
            }

            sourceWindowsDepsJsonPathList.push(depsJsonPath);
            argumentIndex += 1;
            continue;
        }

        throw new Error(`Unsupported argument: ${argument}`);
    }

    let noticeTargetList = targetList;
    if (noticeTargetList.length === 0) {
        noticeTargetList = ["hub"];
        if (sourceWindowsDepsJsonPathList.length > 0) {
            noticeTargetList = ["hub", "source-windows"];
        }
    }
    for (const target of noticeTargetList) {
        if (target !== "hub" && target !== "source-windows") {
            throw new Error(`Unsupported third-party notice target: ${target}`);
        }
    }

    const resultList = shouldCheck
        ? await checkThirdPartyNotices(noticeTargetList, { sourceWindowsDepsJsonPathList })
        : await updateThirdPartyNotices(noticeTargetList, { sourceWindowsDepsJsonPathList });
    for (const result of resultList) {
        const relativePath = result.path.startsWith(repositoryDirectory)
            ? result.path.slice(repositoryDirectory.length + 1)
            : result.path;
        process.stdout.write(`${formatNoticeResultVerb(result.changed, shouldCheck)} ${relativePath}\n`);
    }

    if (shouldCheck && resultList.some(result => result.changed)) {
        process.exitCode = 1;
    }
}

function formatNoticeResultVerb(changed, shouldCheck) {
    if (shouldCheck) {
        return changed ? "Outdated" : "Current";
    }

    return changed ? "Updated" : "Unchanged";
}

if (process.argv[1] === fileURLToPath(import.meta.url)) {
    await main(process.argv.slice(2));
}
