#!/usr/bin/env node
import { readFile, writeFile } from "node:fs/promises";
import { createRequire } from "node:module";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { icons as lucideIcons } from "lucide";

const require = createRequire(import.meta.url);
const scriptDirectory = path.dirname(fileURLToPath(import.meta.url));
const packageDirectory = path.resolve(scriptDirectory, "..");
const outputPath = path.resolve(
    packageDirectory,
    "src/generated/custom-metric-lucide-search-index.generated.ts",
);
// Keeps common technical acronyms readable in generated picker labels, such as
// "CPU" and "TV", instead of title-casing them as "Cpu" and "Tv".
const labelAcronymByPart = new Map([
    ["cpu", "CPU"],
    ["dna", "DNA"],
    ["gpu", "GPU"],
    ["hdmi", "HDMI"],
    ["id", "ID"],
    ["nfc", "NFC"],
    ["qr", "QR"],
    ["ram", "RAM"],
    ["tv", "TV"],
    ["usb", "USB"],
    ["vpn", "VPN"],
]);

const lucidePackageJson = await readPackageJson("lucide/package.json");
const lucideStaticPackageJson = await readPackageJson("lucide-static/package.json");
if (lucidePackageJson.version !== lucideStaticPackageJson.version) {
    throw new Error(
        `lucide (${lucidePackageJson.version}) and lucide-static (${lucideStaticPackageJson.version}) versions must match.`,
    );
}

// lucide-static packages tags.json today, but its README does not document that
// file as a public API. Keep lucide/lucide-static exact-pinned and fail fast
// below so package layout changes cannot silently degrade picker search.
const lucideTags = JSON.parse(await readFile(require.resolve("lucide-static/tags.json"), "utf8"));
validateLucideTags(lucideTags);

const exportNamesByIconId = await readLucideExportNamesByIconId();
const iconIds = Object.keys(lucideTags).sort((left, right) => left.localeCompare(right));

const entries = [];

for (const iconId of iconIds) {
    const exportName = toPascalCase(iconId);
    if (!Object.hasOwn(lucideIcons, exportName)) {
        continue;
    }

    const exportNames = exportNamesByIconId.get(iconId) ?? [exportName];
    entries.push({
        id: iconId,
        label: formatIconLabel(iconId),
        exportName,
        terms: [
            ...splitIconIdTerms(iconId),
            ...lucideTags[iconId],
            ...exportNames
                .filter(name => name !== exportName)
                .flatMap(splitExportNameTerms),
        ].map(normalizeSearchTerm)
            .filter(term => term.length > 0)
            .filter((term, index, terms) => terms.indexOf(term) === index)
            .sort((left, right) => left.localeCompare(right)),
    });
}

const generatedSource = `${[
    "/* eslint-disable */",
    "// <auto-generated>",
    "// Generated by scripts/generate-lucide-icon-index.mjs.",
    "// Do not edit by hand.",
    "// </auto-generated>",
    "",
    "export interface GeneratedCustomMetricLucideIconEntry {",
    "    readonly id: string;",
    "    readonly label: string;",
    "    readonly exportName: string;",
    "    readonly terms: readonly string[];",
    "}",
    "",
    "export const GENERATED_CUSTOM_METRIC_LUCIDE_ICON_ENTRIES = ",
    `${JSON.stringify(entries, undefined, 4)} as const satisfies readonly GeneratedCustomMetricLucideIconEntry[];`,
    "",
].join("\n")}`;

let previousSource;
try {
    previousSource = await readFile(outputPath, "utf8");
} catch {
    previousSource = undefined;
}

if (previousSource !== generatedSource) {
    await writeFile(outputPath, generatedSource, "utf8");
    process.stdout.write(`Wrote ${path.relative(packageDirectory, outputPath)} with ${entries.length} icons.\n`);
} else {
    process.stdout.write(`Lucide icon index is up to date with ${entries.length} icons.\n`);
}

async function readPackageJson(packageJsonSpecifier) {
    return JSON.parse(await readFile(require.resolve(packageJsonSpecifier), "utf8"));
}

async function readLucideExportNamesByIconId() {
    const iconsAndAliasesSource = await readFile(require.resolve("lucide/dist/esm/iconsAndAliases.mjs"), "utf8");
    const exportNamesByIconId = new Map();
    const exportLinePattern = /^export \{ (?<exports>.+) \} from '\.\/icons\/(?<iconId>[^']+)\.mjs';$/gm;

    for (const match of iconsAndAliasesSource.matchAll(exportLinePattern)) {
        const iconId = match.groups.iconId;
        const exportNames = match.groups.exports
            .split(",")
            .map(entry => entry.trim())
            .map(entry => /^default as (?<exportName>[A-Za-z0-9]+)$/.exec(entry)?.groups?.exportName)
            .filter(exportName => exportName !== undefined);

        if (exportNames.length > 0) {
            exportNamesByIconId.set(iconId, exportNames);
        }
    }

    if (exportNamesByIconId.size === 0) {
        throw new Error("Failed to parse lucide/dist/esm/iconsAndAliases.mjs exports.");
    }

    return exportNamesByIconId;
}

function validateLucideTags(tagsByIconId) {
    if (!tagsByIconId || typeof tagsByIconId !== "object" || Array.isArray(tagsByIconId)) {
        throw new Error("lucide-static/tags.json must contain an object.");
    }

    for (const [iconId, tags] of Object.entries(tagsByIconId)) {
        if (!isLucideIconId(iconId)) {
            throw new Error(`lucide-static/tags.json contains invalid icon id: ${iconId}`);
        }

        if (!Array.isArray(tags) || tags.some(tag => typeof tag !== "string")) {
            throw new Error(`lucide-static/tags.json ${iconId} must be a string array.`);
        }
    }
}

function splitIconIdTerms(iconId) {
    return [
        iconId,
        ...iconId.split("-"),
        formatIconLabel(iconId),
    ];
}

function splitExportNameTerms(exportName) {
    const words = exportName.match(/[A-Z]?[a-z]+|[0-9]+|[A-Z]+(?![a-z])/g) ?? [exportName];
    const aliasId = words.map(word => word.toLowerCase()).join("-");
    return [
        aliasId,
        words.join(" "),
    ];
}

function normalizeSearchTerm(value) {
    return value.trim().toLowerCase();
}

function formatIconLabel(iconId) {
    return iconId
        .split("-")
        .map(formatIconLabelPart)
        .join(" ");
}

function formatIconLabelPart(part) {
    return labelAcronymByPart.get(part)
        ?? (part.length === 0 ? part : `${part[0].toUpperCase()}${part.slice(1)}`);
}

function toPascalCase(iconId) {
    return iconId
        .split("-")
        .map(part => part.length === 0 ? part : `${part[0].toUpperCase()}${part.slice(1)}`)
        .join("");
}

function isLucideIconId(value) {
    return /^[a-z0-9]+(?:-[a-z0-9]+)*$/.test(value);
}
